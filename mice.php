<script>
    $(".mice_tab").on("click", (event) => {
        $(".mice_tab").removeClass("active");
        $(".mice_contents").hide();
        if (event.target.dataset.mice_name != undefined) {
            $('.mice_' + event.target.dataset.mice_name).show();
            $(event.target).addClass("active");
        } else if (event.target.parentElement.dataset.mice_name != undefined) {
            $('.mice_' + event.target.parentElement.dataset.mice_name).show();
            $(event.target.parentElement).addClass("active");
        } else if (event.target.parentElement.parentElement.dataset.mice_name != undefined) {
            $('.mice_' + event.target.parentElement.parentElement.dataset.mice_name).show();
            $(event.target.parentElement.parentElement).addClass("active");
        }
    });

    $(".drop_down_overlay").on("click", function (event) {
        $(".drop_down_open").removeClass("show_content");
        console.log(event.target.parentElement.parentElement);
        $(event.target.parentElement.parentElement).toggleClass("show_content");
    });
</script>